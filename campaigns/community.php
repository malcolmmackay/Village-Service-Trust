<p>The VST has formed 73 women's groups in 59 villages, with a total membership of 2167. These constitute the backbone of work to develop village communities.</p>
				<p>The aim has been to unite women villagers to campaign for
better rights and an increased role in decision making. Strategies are discussed to improve the education, health care and economic status of the people. </p>
<p>In every village, two meetings are conducted each month. The Sangams consist of an elected president and other office bearers who organise the meetings.
	Local issues are discussed and steps are taken to resolve them.</p>

<h6>Small Savings</h6>
<p>To reduce the impact of unscrupulous money lenders, the VST has introduced and promoted a small savings scheme. </p>
<p>Each Sangam member saves Rs.30 per month. In return, they have access to loans at very low interest rates. To date, the savings total Rs.540,626.</p>
					
			

