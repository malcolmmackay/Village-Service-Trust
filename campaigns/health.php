					<p>The VST recognises health as a developmental imperative. It is committed to treatment and disease prevention among the poor and deprived. </p>
<p>Over the years, we have worked to improve people's well-being by involving alternate health care strategies that are accessible, appropriate and affordable.</p>
<h5>Health Centers</h5>
<p>In 1999, a community health center was opened in Michaelpayam. A nurse and doctor, working full-time, help 170 villagers every day.</p>
<p>Two years later, a maternal health center was opened in Sakanaickanoor village. Last year, this provided ante-natal and post-natal services to 186 women. In addition, immunisations and treatments for common diseases are given to babies and young children.</p>
					<p>Counselling and support is offered to people who have been subjected to difficult situations such as abuse. </p>
<h5>Disease Prevention and Awarness</h5>
<p>Apart from the counseling at the clinics, the rural poor are educated on various health related topics through regular health classes by Village Health Workers. As a result, people's awareness of hygiene, sanitation, safe drinking water, communicable diseases, diarrhoea, STDs/AIDS, immunisation, family planning and birth spacing has substantially increased. </p>
<p>The Health Committees and self-help groups formed in each target village have stimulated community participation.</p>

<h5>Eye and Dental Camps</h5>
<p>Every year in collaboration with Aravind Hospital, Madurai, the VST organises eye and dental camps to prevent blindness and to promote oral health among the underprivileged. Camps were helded in Dindigul, Kamalapuram and Michaelpalayam to bring oral care including antibiotics to 1454 patients.</p>
<p>Last year, the eye camp held at Sakanaickanoor benefited 2342 school children, of whom 48 received spectacles.</p>

<h5>Promotion of Health among Indigenous Communities</h5>
<p>Through this programme, the VST hopes to bring healthcare within easy and affordable reach of villagers in rural areas. Local people are trained to identify and treat common ailments with locally avilable resources. Last year, 40 women were trained as <i>Community Health Workers</i>.</p>
