				<p>The VST has a range of education programmes for the benefit of children and adults.</p>
<h5>Tuition Centers</h5>
<p>Many homes in the villages are without electricity, so that after school hours, books are rarely opened. Many parents are themselves illiterate, so the children have no one to refer to when in doubt. </p>
<p>The VST operates a network of 36 tuition centers, which benefit 928 children. The centers provide a broad based supplementary education and supervised tuition both before and after school hours. We currently have students from the 1st to XIIth standards.</p>
<p>Apart from academic subjects children are encouraged to take part in indoor games, singing and dancing and other enjoyable activities. Parents meetings, conducted four times a year, strengthen parents participation in the programme and reduce dropout rates.</p>

<h5>Skills Training for Adults</h5>
<p>Technical skills are taught to empower the poorest people to generate a permanent income. Skills taught include weaving, carpentry, typewriting, tailoring, leadership, accounts writing and finance management. In many cases, the people in our target groups have not received any training before. </p>
				
