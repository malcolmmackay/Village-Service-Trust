<h5>Rehabilitation</h5>
<p>Besides the immediate relief measures, the team of VST volunteers, staff and professionals have been with the victims in order to rehabilitate them and bring them back into mainstream development. We have given assistance to fisher people who have lost their houses, belongings, boats, nets and other precious items. </p>

<h5>Trauma Conselling</h5>
<p>The Tsunami had a devastating impact on a great number of women and children who lost their husbands or parents, leaving them traumatised and depressed.</p>
<p>Many people who have lost their dear and near ones experienced trauma while they were in the camps. The bitter memories of Tsunami haunted their minds and they could not give vent to the depressed feelings.</p>
<p>The trained counsellors of VST helped the most depressed victims and enabled them to regain their lost confidence. The one-to-one counselling sessions healed the scars and gradually enabled them to tread in the path of mainstream development. 104 seriously depressed people were counselled to lead a normal life and a number of women and children were given integral counselling to forget their bitter past, allowing them to restart their lives with renewed spirit. Overall, more than 1100 people were given counselling to heal the ill effects of Tsunami and become self-reliant and courageous in their lives.</p>
					
<h5>The Rehabilitation of Srilankan Refugees</h5>
<p>Today, India has 250,000 refugees. Srilankans fled to Tamil Nadu in 1983, after sectarian violence (<a class="external" href="http://www.refugeesinternational.org/content/article/detail/933/">More</a>). Many have been returned, but 81,000 remain in the region, with 75% in government run camps.</p>
<p>There are 3 settlements of Srilankan refugees in the Dindigul District. These people had miserable living conditions. Basic neccessities such as a house, clothes, food and medicines were absent. The VST stepped in with relief measures. We are now contemplating the initiation of more development programmes in these settlements.</p>
