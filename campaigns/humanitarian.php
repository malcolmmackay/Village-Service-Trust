				<h3>Humanitarian Relief</h3>
				<h5>Relief and Rehabilitation Activities for the Tsunami Victims</h5>
					<p>On the 26th December 2004, the tsunami waves devasted the coastal villages of Tamil Nadu. There were huge and grave losses in terms of human lives and properties. The VST was involved in emergency relief operatons in the Kanyakumari district of Tamil Nadu, which was among the worst hit areas. </p>

<h5>Support to Victims in Temporary Camps</h5>
<p>As soon as the disaster occcurred, the surviving fisher people fled from their native villages and sought asylum in the temporary camps. These were organised in schools, churches, marriage halls and other common places.</p>
<p>The VST extended its unstinted support and help to the victims through its committed volunteers, staff and some students. In all the camps, the relief team members of the VST provided food, water, medicines, mats, bed sheets and clothes to the victims who had lost everything in Tsunami. Initially, over 4000 people were helped by relief measures to sustain their livelihood.</p>

<h5>Medical Aid and Treatment</h5>
<p>The VST and it's committed team of doctors, health personnel and volunteers rushed to organise medical camps in the villages of Muttom and Azhikal.</p><p>While escaping from the wretched arms of Tsunami, a great number of men, women and children were wounded. The health team of the VST has delivered immediate medical care and treatment to over 2000 victims. Our medical team is still in the villages providing medical services to the affected people. </p>

